#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	char	c1;
	char	c2;

	if ((int)c < 128)
		write(fd, &c, 1);
	else
	{
		c1 = 192 + c / 64;
		c2 = 128 + c % 64;
		write(fd, &c1, 1);
		write(fd, &c2, 1);
	}
}
