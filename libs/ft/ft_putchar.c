#include "libft.h"

void	ft_putchar(char c)
{
	char	c1;
	char	c2;

	if ((int)c < 128)
		write(1, &c, 1);
	else
	{
		c1 = 192 + c / 64;
		c2 = 128 + c % 64;
		write(1, &c1, 1);
		write(1, &c2, 1);
	}
}
