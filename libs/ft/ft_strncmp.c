#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;

	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	if (!n)
		return (0);
	i = 0;
	while (*a == *b && *a && i < n - 1)
	{
		a++;
		b++;
		i++;
	}
	if (*a != *b)
		return (*a - *b);
	return (0);
}
