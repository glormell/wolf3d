#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*a;
	char	*b;

	a = (char *)s1;
	b = (char *)s2;
	while (*a)
		a++;
	while (*b)
		*a++ = *b++;
	*a = '\0';
	return (s1);
}
