#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;

	a = (char *)s;
	while (*a)
		a++;
	while (a != s)
	{
		if (*a == c)
			return (a);
		a--;
	}
	if (*a == c)
		return (a);
	else
		return (0);
}
