#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*r;

	if (!s)
		return (0);
	if (!(r = (char *)malloc(len + 1)))
		return (0);
	r[len] = '\0';
	while (len--)
		r[len] = *(s + start + len);
	return (r);
}
